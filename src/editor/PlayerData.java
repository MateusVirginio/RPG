package editor;

public class PlayerData {
    public int maxLife;
    public int speed;
    public int startX;
    public int startY;

    public PlayerData(int maxLife, int speed, int startX, int startY) {
        this.maxLife = maxLife;
        this.speed = speed;
        this.startX = startX;
        this.startY = startY;
    }
}